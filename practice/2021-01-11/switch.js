/*********************************
switch case demo
*/
var job = 'student';

switch(job){
	case 'student' :
		console.log("Studying...");
		break;
	case 'programer' :
		console.log("Programing...");
		break;
	case 'designer' :
		console.log("Designing...");
		break;
	default:
		console.log("Something");
}